    private static byte[] aesDecrypt(byte[] secretKey, byte[] encoded) throws Exception {
        byte[] iv = new byte[16];
        System.arraycopy(encoded, 0, iv, 0, 16);
        byte[] data = new byte[encoded.length - 16];
        System.arraycopy(encoded, 16, data, 0, data.length);

        SecretKeySpec keySpec = new SecretKeySpec(secretKey, "AES");
        IvParameterSpec ivSpec = new IvParameterSpec(iv);
        Cipher cipher = Cipher.getInstance("AES/CBC/NoPadding");
        cipher.init(Cipher.DECRYPT_MODE, keySpec, ivSpec);
        byte[] decrypted = cipher.doFinal(data);

        // Remove PKCS7 padding
        int padLen = decrypted[decrypted.length - 1] & 0xFF;
        if (padLen > 0 && padLen <= 16) {
            decrypted = Arrays.copyOf(decrypted, decrypted.length - padLen);
        }
        return decrypted;
    }

