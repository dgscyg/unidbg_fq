    /** Convert unsigned 32-bit int to wrapped signed int. Same as Python's check(). */
    private static int check(long tmp) {
        return (int) (tmp & -1L);
    }

    private static String toHex(int num) {
        return Integer.toHexString(check(num));
    }

    private static String toBinaryString(int num) {
        String s = Integer.toBinaryString(check(num));
        StringBuilder sb = new StringBuilder(32);
        for (int i = 0; i < 32 - s.length(); i++) sb.append('0');
        sb.append(s);
        return sb.toString();
    }

    /** 32-bit binary string, MSB first. */
    private static String bin_type(int num) {
        int v = check(num);
        StringBuilder sb = new StringBuilder(32);
        for (int i = 31; i >= 0; i--) {
            sb.append(((v >> i) & 1) == 1 ? '1' : '0');
        }
        return sb.toString();
    }

    /** Add with carry set if overflow. */
    private int ADDS(int a, int b) {
        long c = (check(a) & -1L) + (check(b) & -1L);
        if (c >= 0L) {
            CF = 1;
        } else {
            CF = 0;
        }
        return check(c);
    }

    private int ADC(int a, int b) {
        long c = (check(a) & -1L) + (check(b) & -1L);
        long d = c + CF;
        return check(d);
    }

    /** Add with carry and set CF. */
    private int ADCS(int a, int b) {
        long c = (check(a) & -1L) + (check(b) & -1L);
        long d = c + CF;
        if (c >= 0L) {
            CF = 1;
        } else {
            CF = 0;
        }
        return check(d);
    }

    private int ANDS(int a, int b) {
        return (int) (check(a) & check(b));
    }

    private int EORS(int a, int b) {
        return (int) (check(a) ^ check(b));
    }

    private int ORRS(int a, int b) {
        return (int) (check(a) | check(b));
    }

    /** Logical left shift. CF = bit at position (32-k) of original. */
    private int LSLS(int num, int k) {
        String bin = bin_type(num);
        CF = bin.charAt(k - 1) == '1' ? 1 : 0;
        return check((long) check(num) << k);
    }

    /** Logical right shift. CF = bit at position (32-k) of original. Note: Python uses the end of binary string. */
    private int LSRS(int num, int k) {
        String bin = bin_type(num);
        CF = bin.charAt(32 - k) == '1' ? 1 : 0;
        return check((check(num) & -1L) >>> k);
    }

    /** Rotate right through carry. Shift right 1, CF goes to MSB. */
    private int RRX(int num) {
        String bin = bin_type(num);
        String s = CF + bin.substring(0, 31);
        return (int) Long.parseLong(s, 2);
    }

    /** Unsigned bit field extract: extract width bits starting at lsb from LSB side. */
    private int UBFX(int num, int lsb, int width) {
        String bin = bin_type(num);
        int start = 32 - lsb - width;
        int end = 32 - lsb;
        return (int) Long.parseLong(bin.substring(start, end), 2);
    }

    /** Unsigned field: extract bottom 8 bits. */
    private int UTFX(int num) {
        return (int) (check(num) & 0xFFL);
    }

    /** Convert 4-byte chunks to int list (little-endian interpretation). */
    private int[] hex_list(int[] content) {
        int[] result = new int[content.length * 4];
        for (int i = 0; i < content.length; i++) {
            String hex = toHex(content[i]);
            while (hex.length() < 8) hex = "0" + hex;
            for (int j = 0; j < 4; j++) {
                String chunk = hex.substring(2 * j, 2 * j + 2);
                result[4 * i + j] = (int) Long.parseLong(chunk, 16);
            }
        }
        return result;
    }

    /** Pack every 4 ints into 1 int (as 8 hex digits). */
    private int[] dump_list(int[] content) {
        int size = content.length;
        int[] result = new int[size / 4];
        for (int i = 0; i < size / 4; i++) {
            StringBuilder sb = new StringBuilder();
            for (int j = 0; j < 4; j++) {
                String hex = toHex(content[4 * i + j]);
                if (hex.length() < 2) hex = "0" + hex;
                sb.append(hex);
            }
            result[i] = (int) Long.parseLong(sb.toString(), 16);
        }
        return result;
    }

    /** Convert list_6B0 from 16 ints to 64 bytes. */
    private int[] hex_C52(int[] list_6B0) {
        int[] list_8D8 = new int[64];
        for (int i = 0; i < 8; i++) {
            int[] pair = {list_6B0[2 * i + 1], list_6B0[2 * i]};
            int[] tmp = hex_list(pair);
            System.arraycopy(tmp, 0, list_8D8, 8 * i, 8);
        }
        return list_8D8;
    }

