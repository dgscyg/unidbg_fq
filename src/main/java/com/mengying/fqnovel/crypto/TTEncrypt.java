    /**
     * Encrypt JSON data using the TT encryption algorithm.
     * Path: JSON string -> gzip compress -> custom block cipher encrypt -> bytes.
     */
    public byte[] encrypt(String jsonData) {
        try {
            // Gzip header (10 bytes): 1f 8b 08 00 00 00 00 00 00 00
            int[] headers = {0x1f, 0x8b, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};

            // Gzip compress
            byte[] compressed = gzipCompress(jsonData.getBytes("UTF-8"));
            int[] data = bytesToInts(compressed);

            setData(data);

            // Overwrite first 10 bytes with custom header
            for (int i = 0; i < headers.length; i++) {
                __content[i] = headers[i];
            }

            // Key derivation from random salt
            int[] list_0B0 = concatIntArrays(calculate(list_9C8), ORD_LIST);
            int[] list_5D8 = calculate(list_0B0);

            int[] list_378 = new int[16];
            int[] list_740 = new int[16];
            System.arraycopy(list_5D8, 0, list_378, 0, 16);
            System.arraycopy(list_5D8, 16, list_740, 0, 16);

            int[] list_378Array = dump_list(list_378);

            // Hash of content
            int[] list_8D8 = calculate(__content);

            // Combine content hash + content
            int[] list_AB0 = concatIntArrays(list_8D8, __content);

            // PKCS7 padding
            int[] list_AB0_padded = pkcs7Pad(list_AB0, 16);
            int[] list_55C = hex_CF8(list_378Array);
            int[] final_list = hex_0A2(list_AB0_padded, list_740, list_55C);

            // Final output: beginning (6) + salt (32) + encrypted
            int[] result = concatIntArrays(
                concatIntArrays(BEGINING, list_9C8),
                final_list
            );

            // Convert longs to signed bytes
            byte[] output = new byte[result.length];
            for (int i = 0; i < result.length; i++) {
                output[i] = (byte) (result[i] & 0xFF);
            }

            return output;
        } catch (Exception e) {
            throw new RuntimeException("TTEncrypt encrypt failed", e);
        }
    }

    /**
     * Decrypt TT-encrypted data.
     * Path: parse header -> extract salt + encrypted body -> AES-128-CBC decrypt -> gzip decompress.
     */
    public String decrypt(byte[] data) {
        try {
            int[] raw = bytesToInts(data);

            // Parse header: 38 bytes (6 magic + 32 salt)
            int[] content_raw = raw;
            setData(content_raw);

            int[] encryptedBody = new int[__content_raw.length - 38];
            System.arraycopy(__content_raw, 38, encryptedBody, 0, encryptedBody.length);

            int[] salt = new int[32];
            System.arraycopy(__content_raw, 6, salt, 0, 32);
            list_9C8 = salt;

            __content = changeByteArrayToLong(encryptedBody);

            // Key derivation
            int[] list_0B0 = concatIntArrays(calculate(list_9C8), ORD_LIST);
            int[] list_5D8 = calculate(list_0B0);

            int[] list_378 = new int[16];
            System.arraycopy(list_5D8, 0, list_378, 0, 16);
            int[] list_378Array = dump_list(list_378);

            // AES key (32 bytes = 64 hex chars)
            byte[] aesKey = intsToBytes(hex_list(list_378Array));

            // AES-128-CBC decrypt
            byte[] contentBytes = intsToBytes(__content);
            byte[] decrypted = aesDecrypt(aesKey, contentBytes);

            // Skip first 64 bytes (padding), then gzip decompress
            byte[] toDecompress = new byte[decrypted.length - 64];
            System.arraycopy(decrypted, 64, toDecompress, 0, toDecompress.length);

            byte[] result = gzipDecompress(toDecompress);
            return new String(result, "UTF-8");
        } catch (Exception e) {
            throw new RuntimeException("TTEncrypt decrypt failed", e);
        }
    }

